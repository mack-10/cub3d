#include "cub3d.h"

void	free_double(char **s)
{
	int i;

	i = 0;
	while (s[i])
	{
		free(s[i]);
		s[i++] = NULL;
	}		
	free(s);
	s = NULL;
}

void	free_one(char *s)
{
	free(s);
	s = NULL;
}